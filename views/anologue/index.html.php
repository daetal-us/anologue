<h1 class="title">anologue</h1>

<p>i need to have a conversation with one or more people.</p>
<p>there's <strong>email</strong>, but i get pretty sick of the copied text that quickly gets out of control, the signuatures. and, i get so much crap in my inbox as-is, it can be cumbersome to try and isolate relevant emails and the important parts of them.</p>
<p>i don't like <strong>instant messaging</strong>. there are so many clients out there, and, sure there are tools to help them come together, but there are some people that will never us im (and i don't blame them.)</p>
<p><strong>twitter</strong> is right out.</p>
<p>then, there's one of my favorites: <strong>irc</strong>. but, let's face it, it's mostly for nerds.</p>

<h2 class="sub">in comes anologue</h2>

<img id="anologue-excellent" src="/img/excellent.jpg" alt="excellent!" />
<p>it's like comments, meets im, meets irc, meets <a href="http://pastium.org">your favorite paste app</a>, meets instant coffee.</p>
<p>actually, instant coffee sucks.</p>
<p>with anologue you can quickly and easily enjoy an anonymous (or not) dialogue between one or more persons (sorry, androids.)</p>
<p><strong>no accounts. no installations. no way?! <em>yes, way!</em></strong></p>
<p><a href="/new">your `chat room` is created in the time it takes for this link to load</a>. invite whoever you want by giving them your unique link, and chat away. if you can type, you can use anologue. and, judging by the average commenter on youtube, i'd say that includes all but those without electricity. for the more technically saavy, you may appreciate the implementation of markdown and use of gravatar.</p>

